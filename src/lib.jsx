// Simple cache to avoid fetching the same data twice
const dataCache = new Map();

// This function fetches data and caches it so we don't make duplicate requests
async function fetchWithCache(cacheKey, fetchFunction) {
  // If we already have this data cached, return it
  if (dataCache.has(cacheKey)) {
    return dataCache.get(cacheKey);
  }

  // Otherwise, fetch the data
  const fetchPromise = fetchFunction();
  dataCache.set(cacheKey, fetchPromise);

  try {
    const result = await fetchPromise;
    return result;
  } catch (error) {
    // If the fetch fails, remove it from cache so we can try again
    dataCache.delete(cacheKey);
    throw error;
  }
}

// ============================================
// STORAGE HELPERS - LocalStorage functions
// ============================================

// We use 'atlasora' as a prefix for all our localStorage keys
const STORAGE_PREFIX = "atlasora";

// Read data from localStorage
// If the key doesn't exist or parsing fails, return the fallback value
export function readFromStorage(key, fallbackValue) {
  try {
    const fullKey = `${STORAGE_PREFIX}:${key}`;
    const rawData = localStorage.getItem(fullKey);

    if (rawData === null) {
      return fallbackValue;
    }

    return JSON.parse(rawData);
  } catch (error) {
    // If something goes wrong, just return the fallback
    return fallbackValue;
  }
}

// Write data to localStorage
// If storage is full or unavailable, we fail silently
export function writeToStorage(key, value) {
  try {
    const fullKey = `${STORAGE_PREFIX}:${key}`;
    const jsonString = JSON.stringify(value);
    localStorage.setItem(fullKey, jsonString);
  } catch (error) {
    // Storage might be full or in private mode - just ignore
    console.warn("Could not save to storage:", error);
  }
}

// Remove a specific key from localStorage
export function removeFromStorage(key) {
  const fullKey = `${STORAGE_PREFIX}:${key}`;
  localStorage.removeItem(fullKey);
}

