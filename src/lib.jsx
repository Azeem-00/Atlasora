// Search for countries by name
// If the search fails, we search through our fallback data instead
export async function searchCountriesByName(searchTerm) {
  if (!searchTerm?.trim()) {
    return [];
  }

  try {
    const response = await fetch(
      `${COUNTRIES_API_URL}/name/${encodeURIComponent(searchTerm)}?fields=${COUNTRY_FIELDS_TO_FETCH}`,
    );

    if (!response.ok) {
      return [];
    }

    return response.json();
  } catch (error) {
    console.error("Search API Error, using fallback:", error);

    // Search through our fallback data
    const lowerSearchTerm = searchTerm.toLowerCase();
    return FALLBACK_COUNTRIES.filter((country) =>
      country.name.common.toLowerCase().includes(lowerSearchTerm),
    );
  }
}

