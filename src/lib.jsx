// Find nearby attractions around a location using Wikipedia's geosearch
// Returns places of interest within a certain radius of coordinates
export async function getAttractionsNear(
  latitude,
  longitude,
  radiusInMeters = 30000,
  maxResults = 12,
) {
  return fetchWithCache(`geosearch-${latitude}-${longitude}`, async () => {
    const searchParams = new URLSearchParams({
      action: "query",
      list: "geosearch",
      gscoord: `${latitude}|${longitude}`,
      gsradius: String(Math.min(radiusInMeters, 10000)), // Wikipedia limits to 10km
      gslimit: String(maxResults),
      format: "json",
      origin: "*",
    });

    const response = await fetch(
      `${WIKIPEDIA_API_URL}?${searchParams.toString()}`,
    );

    if (!response.ok) {
      throw new Error("Failed to load attractions");
    }

    const data = await response.json();
    return data.query?.geosearch ?? [];
  });
}

// Get detailed information for multiple Wikipedia articles at once
// Used to get descriptions and thumbnails for attractions
export async function getWikipediaDetailsBatch(articleTitles) {
  if (!articleTitles.length) {
    return {};
  }

  const searchParams = new URLSearchParams({
    action: "query",
    prop: "extracts|pageimages",
    exintro: "1",
    explaintext: "1",
    exsentences: "2",
    piprop: "thumbnail",
    pithumbsize: "400",
    titles: articleTitles.join("|"),
    format: "json",
    origin: "*",
  });

  const response = await fetch(
    `${WIKIPEDIA_API_URL}?${searchParams.toString()}`,
  );

  if (!response.ok) {
    return {};
  }

  const data = await response.json();
  return data.query?.pages ?? {};
}

// ---------- Unsplash photography ----------

const FALLBACK_IMAGE_SEEDS = [
  "travel",
  "mountains",
  "coastline",
  "old-town",
  "skyline",
  "desert",
  "forest",
  "harbor",
];

export function fallbackImage(seed, w = 800, h = 600) {
  const s = encodeURIComponent(seed || FALLBACK_IMAGE_SEEDS[0]);
  return `https://picsum.photos/seed/${s}/${w}/${h}`;
}

export async function getUnsplashPhoto(
  query,
  { width = 800, height = 600 } = {},
) {
  if (!UNSPLASH_API_KEY) {
    return {
      url: fallbackImage(query, width, height),
      credit: null,
      isFallback: true,
    };
  }
  return fetchWithCache(`unsplash-${query}-${width}x${height}`, async () => {
    try {
      const params = new URLSearchParams({
        query,
        orientation: width >= height ? "landscape" : "portrait",
        content_filter: "high",
      });
      const res = await fetch(
        `https://api.unsplash.com/photos/random?${params.toString()}`,
        {
          headers: { Authorization: `Client-ID ${UNSPLASH_API_KEY}` },
        },
      );
      if (!res.ok) throw new Error("Unsplash error");
      const data = await res.json();
      return {
        url: `${data.urls.raw}&w=${width}&h=${height}&fit=crop&q=80`,
        credit: { name: data.user?.name, link: data.user?.links?.html },
        isFallback: false,
      };
    } catch {
      return {
        url: fallbackImage(query, width, height),
        credit: null,
        isFallback: true,
      };
    }
  });
}

export function flagEmoji(cca2) {
  if (!cca2) return "";
  return cca2
    .toUpperCase()
    .replace(/./g, (char) => String.fromCodePoint(127397 + char.charCodeAt(0)));
}

export function formatCoords(lat, lng) {
  if (lat == null || lng == null) return "—";
  const ns = lat >= 0 ? "N" : "S";
  const ew = lng >= 0 ? "E" : "W";
  return `${Math.abs(lat).toFixed(2)}°${ns} ${Math.abs(lng).toFixed(2)}°${ew}`;
}
